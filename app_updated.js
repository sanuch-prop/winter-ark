function formatTraining(lines) {
  if(!lines || !lines.length) return '';
  const parts = lines.map(t=>t.trim()).filter(Boolean).map(txt=>{
    const isHint = /^подсказка/i.test(txt);
    return `<li class="${isHint ? 'hint' : 'exo'}">${txt}</li>`;
  });
  return `<ul>` + parts.join('') + `</ul>`;
}

function loadState() { try { return JSON.parse(localStorage.getItem(STORAGE_KEY)) || {}; } catch(e){ return {}; } }
function saveState(s) { localStorage.setItem(STORAGE_KEY, JSON.stringify(s)); }
let state = loadState();
if(!state.days) state.days = {};
if(state.currentDayIndex == null) state.currentDayIndex = 0;

function getDayState(idx){
  if(!state.days[idx]) state.days[idx] = { items: defaultDayTasks(idx).map(it=>({...it, done:false})) };
  return state.days[idx];
}

function setDayIndex(idx){
  if(idx < 0) idx = 0;
  if(idx > TOTAL_DAYS-1) idx = TOTAL_DAYS-1;
  state.currentDayIndex = idx;
  saveState(state);
  render();
}

function computeProgress(items){
  const actionable = items.filter(i=>i.count && !i.note);
  if(actionable.length===0) return 0;
  const done = actionable.filter(i=>i.done).length;
  return Math.round(done * 100 / actionable.length);
}

function computeAllProgress(){
  let total = 0;
  for(let d=0; d<TOTAL_DAYS; d++) total += computeProgress(getDayState(d).items);
  return Math.round(total / TOTAL_DAYS);
}

function ringSet(el, pct){
  const circle = el.querySelector('circle.fg');
  const dash = 119;
  circle.style.strokeDashoffset = String(dash - dash * Math.min(pct,100)/100);
}

function makeItemLi(item){
  const li = document.createElement('li'); li.className='item';
  const cb = document.createElement('input'); cb.type='checkbox'; cb.checked=!!item.done; cb.disabled = !!item.note;
  cb.addEventListener('change', ()=>{ item.done = cb.checked; saveState(state); updateProgressUI(); });
  const txt = document.createElement('div'); txt.className='txt';
  const title = document.createElement('div'); title.className='title'; title.textContent = item.title; txt.appendChild(title);
  if(item.sub && item.sub.trim()){ const sub = document.createElement('div'); sub.className='sub'; sub.innerHTML = item.sub; txt.appendChild(sub); }
  li.appendChild(cb); li.appendChild(txt);
  return li;
}

function renderChecklist(){
  const st = getDayState(state.currentDayIndex);
  const ul = document.getElementById('checklist'); ul.innerHTML='';
  st.items.forEach(it => ul.appendChild(makeItemLi(it)));
  saveState(state);
  updateProgressUI();
}

function updateProgressUI(){
  const idx = state.currentDayIndex;
  const today = computeProgress(getDayState(idx).items);
  document.getElementById('todayPct').textContent = today + '%';
  ringSet(document.getElementById('todayRing'), today);
  const all = computeAllProgress();
  document.getElementById('allPct').textContent = all + '%';
  ringSet(document.getElementById('allRing'), all);
  document.getElementById('dayLabel').textContent = 'День ' + (idx+1);
  document.getElementById('dayOf90').textContent = (idx+1) + ' / ' + TOTAL_DAYS;
  buildDayGrid();
}

function renderWinterArc(){
  const tabs = document.getElementById('winterTabs');
  const tableWrap = document.getElementById('winterTable');
  tabs.innerHTML = ''; tableWrap.innerHTML='';
  const names = Object.keys(APP_DATA.winterarc);
  if(names.length===0){ tableWrap.textContent = 'Нет данных WinterArc (Excel не распознан).'; return; }
  names.forEach((name, idx)=>{
    const btn = document.createElement('button'); btn.textContent = name; if(idx===0) btn.classList.add('active');
    btn.addEventListener('click',()=>{ [...tabs.children].forEach(b=>b.classList.remove('active')); btn.classList.add('active'); showSheet(name); });
    tabs.appendChild(btn);
  });

  function showSheet(name){
    const raw = APP_DATA.winterarc[name];
    // Move 'Порог' near left if exists
    let cols = raw.columns.slice();
    let rows = raw.rows.map(r=>r.slice());
    const targetIdx = cols.findIndex(c=>/порог/i.test(c));
    if(targetIdx > -1 && targetIdx !== 2) {
      const colName = cols.splice(targetIdx,1)[0];
      cols.splice(2,0,colName);
      rows = rows.map(r=>{ const v = r.splice(targetIdx,1)[0]; r.splice(2,0,v); return r; });
    }
    const table = document.createElement('table');
    const thead = document.createElement('thead');
    const trh = document.createElement('tr');
    cols.forEach(c=>{ const th = document.createElement('th'); th.textContent = c; trh.appendChild(th); });
    thead.appendChild(trh); table.appendChild(thead);
    const tb = document.createElement('tbody');
    rows.forEach(r=>{ const tr=document.createElement('tr'); r.forEach(v=>{ const td=document.createElement('td'); td.textContent = v; tr.appendChild(td); }); tb.appendChild(tr); });
    table.appendChild(tb);
    tableWrap.classList.toggle('fit', /Трекер|компакт|Недельные цели/i.test(name));
    tableWrap.innerHTML=''; tableWrap.appendChild(table);
  }
  showSheet(names[0]);
}

function bindNav(){
  document.querySelectorAll('.bottombar button').forEach(btn=>{
    btn.addEventListener('click', ()=>{
      const tab = btn.dataset.tab;
      document.querySelectorAll('.bottombar button').forEach(b=>b.classList.remove('active'));
      btn.classList.add('active');
      document.querySelectorAll('.tab').forEach(t=>t.classList.remove('active'));
      document.getElementById(tab).classList.add('active');
    });
  });
  document.querySelector('[data-tab="tab-day"]').dataset.tab = 'tab-day';
  document.querySelector('[data-tab="tab-winterarc"]').dataset.tab = 'tab-winterarc';
  document.getElementById('prevDay').onclick = ()=> setDayIndex(state.currentDayIndex - 1);
  document.getElementById('nextDay').onclick = ()=> setDayIndex(state.currentDayIndex + 1);

  // Picker
  document.getElementById('openPicker').onclick = ()=> document.getElementById('dayPicker').classList.remove('hidden');
  document.getElementById('closePicker').onclick = ()=> document.getElementById('dayPicker').classList.add('hidden');
}

function buildDayGrid(){
  const grid = document.getElementById('dayGrid');
  if(!grid) return;
  grid.innerHTML = '';
  for(let d=0; d<TOTAL_DAYS; d++){
    const pct = computeProgress(getDayState(d).items);
    const btn = document.createElement('button');
    btn.innerHTML = 'День ' + (d+1) + '<span class="mini">' + pct + '%</span>';
    if(d === state.currentDayIndex) btn.style.outline = '2px solid var(--accent)';
    btn.onclick = ()=>{ setDayIndex(d); document.getElementById('dayPicker').classList.add('hidden'); };
    grid.appendChild(btn);
  }
}

function render(){
  renderChecklist();
  renderWinterArc();
  updateProgressUI();
}

bindNav();
