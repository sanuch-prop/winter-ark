function isDeload(dayIndex){
  // dayIndex is 0-based, weeks are 1..13; deload weeks: 4, 8, 12
  const week = Math.floor(dayIndex/7) + 1;
  return [4,8,12].includes(week);
}

function morningLabel(dayIndex){
  const dow = (dayIndex % 7) + 1; // 1=Mon..7=Sun, assuming Day1=Mon
  if(dow===3 || dow===5) return 'Утро — кардио (улица): выполнено';
  if(dow===7) return 'Утро — восстановление: выполнено';
  return 'Утро — кардио (зал): выполнено';
}

function morningPlanItems(dayIndex){
  const dow = (dayIndex % 7) + 1; // 1=Mon..7=Sun
  const deload = isDeload(dayIndex);
  const out = ['Разминка суставная 5–7 мин.'];

  function addWalk(minLo, minHi, incline, speed, zone, isOutdoor){
    const mins = deload ? [Math.max(30, Math.round(minLo*0.6)), Math.max(40, Math.round(minHi*0.7))] : [minLo, minHi];
    const inclineTxt = isOutdoor ? 'уклон 0–2%' : `уклон ${incline}`;
    const label = isOutdoor ? 'Ходьба на улице' : 'Ходьба на беговой дорожке';
    out.push(`${label}: ${mins[0]}–${mins[1]} мин, ${inclineTxt}, скорость ${speed} км/ч (${zone}).`);
  }

  function addStairs(minLo, minHi){
    if(!isDeload(dayIndex)){
      out.push(`Ступеньки: ${minLo}–${minHi} мин (RPE 6/10).`);
    }
  }

  // Pattern per TЗ
  if(dow===1){ // Пн
    addWalk(90,120,'8–10%', '5–6', 'Z2', false);
  }else if(dow===2){ // Вт
    addWalk(60,90,'6–8%', '4.8–5.5', 'Z2', false);
  }else if(dow===3){ // Ср (улица)
    addWalk(90,120,'6–8%', '4.8–5.6', 'Z2', true);
  }else if(dow===4){ // Чт + ступеньки
    addWalk(90,120,'8–10%', '5–6', 'Z2', false); addStairs(20,30);
  }else if(dow===5){ // Пт (улица)
    addWalk(90,120,'6–8%', '4.8–5.6', 'Z2', true);
  }else if(dow===6){ // Сб + ступеньки
    addWalk(90,120,'8–10%', '5–6', 'Z2', false); addStairs(20,30);
  }else if(dow===7){ // Вс восстановление
    const mins = deload ? [35,45] : [45,60];
    out.push(`Ходьба (ровно): ${mins[0]}–${mins[1]} мин, уклон 0–1%, скорость 4.8–5.3 км/ч (Z1–Z2).`);
    out.push('Дыхание/мобилити 5–7 мин.');
  }

  out.push('Растяжка 3–5 мин (икры/бедро/ягодицы).');

  // Калории на длинных сессиях
  const longMorning = (dow!==2 && dow!==7); // все кроме вт и вс
  if(longMorning){
    out.push('При необходимости добрать +600–1200 ккал (углеводы) в течение дня.');
  }
  return out;
}
